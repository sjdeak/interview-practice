class Solution:
  def sampleStats(self, count):  # -> List[float]
    data = []
    minimum, maximum = inf, -inf
    totalSum, totalCnt = 0, 0
    for n, cnt in enumerate(count):
      if cnt:
        minimum = min(minimum, n)
        maximum = max(maximum, n)
        data.append((n, cnt))
        totalCnt += cnt
        totalSum += n * cnt

    mean = totalSum / totalCnt
    mode = max(data, key=itemgetter(1))[0]

    if totalCnt % 2:
      medianIndexes = [(totalCnt // 2) + 1]  # 注意下标从1开始  第1个数 第2个数 第3个数...
    else:
      medianIndexes = [totalCnt // 2, (totalCnt // 2) + 1]

    print('medianIndexes:', medianIndexes)

    medianDict = {}  # medianIndex => 这个数具体是几

    for mi in medianIndexes:
      acc = 0
      for (n, cnt) in data:
        acc += cnt
        if mi <= acc and mi not in medianDict:
          medianDict[mi] = n

    print('medianDict:', medianDict)
    median = sum(medianDict.values()) / len(medianDict)

    return list(map(float, [minimum, maximum, mean, median, mode]))


